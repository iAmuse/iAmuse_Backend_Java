package com.iamuse.admin.VO;

public class Card {
	
	private String card_number;
	private String cvv_number;
	private String first_name;
	private String expiry_month;
	private String expiry_year;
	private String street;
	private String city;
	private String state;
	private String country;
	private String zip;
	private String payment_gateway;
	
	public String getCard_number() {
		return card_number;
	}
	public void setCard_number(String card_number) {
		this.card_number = card_number;
	}
	public String getCvv_number() {
		return cvv_number;
	}
	public void setCvv_number(String cvv_number) {
		this.cvv_number = cvv_number;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getExpiry_month() {
		return expiry_month;
	}
	public void setExpiry_month(String expiry_month) {
		this.expiry_month = expiry_month;
	}
	public String getExpiry_year() {
		return expiry_year;
	}
	public void setExpiry_year(String expiry_year) {
		this.expiry_year = expiry_year;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getPayment_gateway() {
		return payment_gateway;
	}
	public void setPayment_gateway(String payment_gateway) {
		this.payment_gateway = payment_gateway;
	}
	
	

}
